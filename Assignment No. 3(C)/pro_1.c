// Q1.Scan a number from user.Print correct input if it lies between 10 and 20.

#include<stdio.h>
int main ()
{
	int num;
	puts("Enter the Number");
	scanf("%d",&num);
	
	if(num>=10 && num<=20)
	{
		puts("Correct Input");
	}
	else
	{
		puts("Wrong input");
	}
	
}
	
	
	
	
	
	
	
	
	

	

