//Q.5 Scan a number from user . print its square and cube .

#include<stdio.h>
int main()
{
	int num,sq,cb;
	printf("\nEnter the number :");
	scanf("%d",&num);
	sq = num*num;
	cb = num*num*num;
	printf("\n The square of number = %d",sq);
	printf("\n The cube of number = %d",cb);
	
	
	
}

